        /// <summary>
        /// Sends a message using a server command.
        /// </summary>
        /// <param name="message">The message to send.</param>
        public void SendBO2Message(string message)
        {
            // check if the console is connected
            if (jtag.IsConnected())
            {
                // send a message if the console is connected
                jtag.CallVoid((uint)BlackOps2MiscOffset.DvSendServerCommand, [-1, 0, $"; \"{message}\""]);
            }
        }

        public void GhostsUnlockAll()
        {
            // check if the console is connected
            if (jtag.IsConnected()) 
            {
                // set memory in the game so you have everything unlocked in COD Ghosts
                jtag.DebugTarget.SetMemory((uint)GhostGeneralOffset.Entry + 0x42E4, (uint)GhostsUnlockAllOffset.Length, GhostsUnlockAllOffset, out dataNum);
            }
        }
